/* USER CODE BEGIN PFP */
int8_t recover_from_task_fault(){
  return 1;
}

void my_fault_handler_c( sContextStateFrame *frame ){

  volatile uint32_t *cfsr = (volatile uint32_t *)0xE000ED28;
  const uint32_t usage_fault_mask = 0xffff0000;
  const int8_t non_usage_fault_occourred = ( *cfsr & ~usage_fault_mask ) != 0;

  const int8_t faulted_from_exception = ((frame->xpsr & 0xFF) != 0);

  // If debugging, halt it right away
  // HALT_IF_DEBUGGING();

  NVIC_SystemReset();

  // If is undercoverable fault
  if ( faulted_from_exception || non_usage_fault_occourred ) {

    volatile uint32_t *aircr = ( volatile uint32_t * )0xE000ED0C;
    *aircr = (0x05FA << 16) | 0x1 << 2;
    while(1){};

  } else {
    *cfsr |= *cfsr;
    frame->return_address = (uint32_t)recover_from_task_fault;
    frame->lr=0xDEADBEEF;
    frame->xpsr = (1 << 24);
  }
}
